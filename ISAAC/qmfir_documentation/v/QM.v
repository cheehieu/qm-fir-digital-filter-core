             ConstantI_d1[IWIDTH-1:0] <= {(IWIDTH){1'b0}};
          end
        else
          begin
             ConstantR_d1[IWIDTH-1:0] <= ConstantR[IWIDTH-1:0] ;
             ConstantI_d1[IWIDTH-1:0] <= ConstantI[IWIDTH-1:0] ;
          end
     end

   
   //**************************************************************************//
   //*                           Counters                                     *//
   //**************************************************************************//
