     if (ARST)
       begin
          counter1[6:0] <= 7'b000_0000;
          counter2[6:0] <= 7'b000_0000;
          counter3[6:0] <= 7'b000_0000;
       end
     else
       begin
          counter1[6:0] <= InputValid ?  
                           ((counter1[6:0] + K1) > 119) ? counter1[6:0]+K1-120
                             : counter1[6:0] + K1 
                           :counter1[6:0];
          counter2[6:0] <= InputValid ?  
                           ((counter2[6:0] + K2) > 119) ? counter2[6:0]+K2-120
                             : counter2[6:0] + K2 
                           :counter2[6:0];
          counter3[6:0] <= InputValid ?  
                           ((counter3[6:0] + K3) > 119) ? counter3[6:0]+K3-120
                             : counter3[6:0] + K3 
                           :counter3[6:0];
       end
   
   
