`timescale 1 ns / 100 ps

module QM
  (/*AUTOARG*/
   // Outputs
   Real1, Real2, Real3, Imag1, Imag2, Imag3, OutputValid,
   // Inputs
   CLK, ARST, ConstantR, ConstantI, InputValid, K1, K2, K3
   );


   //**************************************************************************//
   //*                         Declarations                                   *//
   //**************************************************************************//
   // DATA TYPE - PARAMETERS
